#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int len;

	if (!s)
		return ;
	i = 0;
	len = ft_strlen(s);
	while (s[i] != '\0')
	{
		s[i] = '\0';
		if (i == len)
			return ;
		i++;
	}
}
